2. 제약조건 5가지 종류
    1) PRIMARY KEY(PK) : 기본 키 - NOT NULL + UNIQUE
    2) NOT NULL(NN) : 필수 입력 - 해당 컬럼에 NULL을 허용하지 않음
    3) CHECK(CK) : 유일성 - 해당 컬럼의 값은 테이블 내에서 중복 될 수 없음. 단, NULL 값은 여러 개 허용
    4) FOREIGN KEY(FK) : 외래 키(참조) - 다른 테이블(부모)의 PK나 UK를 참조
*/
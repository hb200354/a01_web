# 오라클 제약조건(CONSTRAINTS)
1. 오라클의 제약조건은 테이블에 데이터를 입력, 수정, 삭제 할 때, 데이터의 무결성을 지키기 위해
    설정하는 강력한 규칙(위반데이터 위 입력, 수정, 삭제 처리되지 않는다) 이다.
